public class Luas extends BangunDatar {

    public void persegi1(double sisi1, double sisi2, double sisi3) {
        double hasilnya = sisi1 * sisi2 * sisi3;
        System.out.println("Luas bangun persegi adalah " + hasilnya);
    }

    public void trapesium1(double sisiT1, double sisiT2, double sisiT3) {
        double hasilnya1 = 0.5 * (sisiT1 + sisiT2) * sisiT3;
        System.out.println("Luas bangun persegi adalah " + hasilnya1);
    }
}
